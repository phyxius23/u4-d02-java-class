public class Product {

  // attributi di istanza
  int codeProduct;
  String descriptionProduct;
  double priceProduct;
  int piecesAvailable;

  // costruttori
  public Product(int cp, String dp, double pp, int pa) {
    this.codeProduct = cp;
    this.descriptionProduct = dp;
    this.priceProduct = pp;
    this.piecesAvailable = pa;
  }
}
